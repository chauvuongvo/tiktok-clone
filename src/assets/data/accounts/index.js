const accounts = {
  followings,
  recommends,
  user,

  fullAccountList: {
    data: fullAccountList,
    getAccount: function (userId) {
      const account = this.data.find((item) => {
        if (item['id']) return item.id === userId;
        return item.user.id === userId;
      });
      return !!account ? account : {};
    },
  },
  followingFullList: {
    data: followingFullList,
    getRandomUser: function () {
      const indexRandom = Math.trunc(Math.random() * this.data.length);
      // return indexRandom;
      return this.data[indexRandom].user.id;
    },
  },
};

export default accounts;
